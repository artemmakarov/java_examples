// цикл for 
class ForDemo {
	public static void main (String args[]) {
		int count;
		for (count = 0; count <=5;count ++)
			System.out.println ("This is count " + count);
			System.out.println ("Done!");
			System.out.println();

			for (count = 10; count >= 0; count --)
				System.out.println ("Decrement iteration " + count); 
	}
}